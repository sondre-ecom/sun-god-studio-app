export const HEADLINE_CRAFT = `
LINE-LEVEL CRAFT — run EVERY hook, on-screen line, and CTA through this. This is where "mid" copy becomes great.

THE 3 RULES (gate every key line; 3 yeses = keep it, 3 nos = rewrite):
  1. CAN I VISUALIZE IT? If you can't see it, you won't remember it. "Charging pitbull" sticks; "a better way" fades. Abstract you can't drop on your foot.
  2. CAN I FALSIFY IT? Is it provably true-or-false, specific? A falsifiable line puts the writer's head on the block — that's why ears prick up. Vague safe lines say nothing.
  3. CAN NOBODY ELSE SAY THIS? "Never write an ad a competitor could sign." If a rival brand could put their logo on your line, kill it.

CONCRETE > ABSTRACT. Take the abstract word and zoom in until you hit a real object you can see. Never "results / progress / definition" — name the exact visible thing ("the line down your stomach", "your jaw in the mirror", "the 6am bathroom check").

DON'T TALK — POINT. Describing uses adjectives ("effective", "powerful"). Pointing uses proof: a label, a chart, a number, a photo, a date. Pointing takes research — that's exactly why it beats adjectives. Never assert what you can show.

LEAD WITH A FACT. A fact guarantees you said something; behind every fact is a story. Precision = credibility ("hit 7 of 11 fairways" beats "played badly"). When in doubt, give a fact. The first line's only job is to earn the second line.

THE CRAFT TOOLBOX:
  • Juxtaposition / contrast — pair two things that shouldn't go together ("supermodels in London and dads in Ohio"; "make 2 months' salary last forever"). The tension is the memorability.
  • Metonymy — swap the literal word for a visual one ("1,000 songs in your pocket", not "media player").
  • Alliteration / rhythm as memory glue ("designed to be deleted").
  • Make them feel smart, not sold — flatter the identity ("for people who inherited brains, not wealth"), never the product.
  • The 2-second test — if the line/frame isn't understood in two seconds, rework it.
  • Comparison anchors — explain the unknown via two knowns ("tougher than an F-150, faster than a 911").
  • Expand the timeframe to grow a number ("you'll spend 700 hours in the gym this year — spend 10 seconds protecting how it looks").
  • Parallelism — repeated structure makes lines land ("raise twice, hire twice, spend twice").

CONFLICT IS THE ENGINE. Every memorable line hinges on a "but" (without writing "but"). Position against an enemy — an Approach (a different way to solve it), a Belief (they think X, the truth is Y), or a Competitor. Relativity sells: name what everyone else does, THEN your alternative.

ONE IDEA. Idea strength is inversely proportional to scope. "And" is a red flag — one ad, one claim, one CTA. Subtract everything that isn't the idea.

SINCERE SMALL CLAIM > HYPE BIG CLAIM. "See the line by day 21" (specific, falsifiable, believable) beats "transform your body" (vague, unbelievable). Believability converts; hype gets scrolled.

WRITE SIMPLY = REWRITE. Any word not working for you is working against you. Cut two-line sentences to one. Bleed the ink dry.
`.trim();
