export const EVOLVE_PLAYBOOK = `
THE PERSUASION PLAYBOOK — think like one of the best direct-response marketers alive. Apply this to every concept, hook, and scene.

1. YOU DON'T CREATE DESIRE — YOU CHANNEL IT. The desire already exists in the prospect, by the millions. You can't manufacture it; you focus existing mass desire onto this product. Find the dominant desire and point it at the mechanism. Never try to "convince" — redirect what they already want.

2. ENTER THE CONVERSATION ALREADY IN THEIR HEAD. Don't start a new message — join the one they're already having with themselves. The hook should feel like you read their mind.

3. AWARENESS — match the lead to where the prospect is:
   • Unaware (doesn't know they have the problem) → lead with a story / symptom / curiosity, never the product.
   • Problem-Aware (feels the pain, not the fix) → agitate the problem, then reveal the mechanism.
   • Solution-Aware (knows fixes exist) → lead with YOUR mechanism and why it's different.
   • Product-Aware (knows your product) → lead with proof, offer, risk-reversal.
   • Most-Aware → lead with the deal.
   Pitch above their awareness and they bounce; below it and you bore them.

4. MARKET SOPHISTICATION — match the claim to how tired the market is:
   • Stage 1 (new market): a plain direct claim works.
   • Stage 2: enlarge/specify the claim (bigger, faster, easier).
   • Stage 3 (claims exhausted): introduce a NEW MECHANISM — the "how" that makes the claim believable again. Most health/supplement markets live here → LEAD WITH MECHANISM.
   • Stage 4: make your mechanism bigger/simpler than theirs.
   • Stage 5 (mechanism exhausted): identification & experience — sell the identity and the feeling; become the brand they belong to.

5. MASS DESIRE has three dimensions — aim at the strongest you can honestly serve: urgency/intensity (how badly, now), staying power (how long it lasts), scope (how many feel it).

6. SELL THE REAL DESIRE UNDER THE SURFACE WANT. They don't want the product, or even the result — they want what the result MEANS (vindication, to feel seen, to stop feeling betrayed by their body, to belong). Sell the deeper transformation.

7. THE PERSUASION ARC (the spine of the ad):
   HOOK (callout / pattern-interrupt that enters their conversation) → PROBLEM (name + agitate the felt pain) → REFRAME ("it's not X, it's Y" — relieves blame, opens the door) → MECHANISM (the simple why/how) → PROOF (recognized lived proof first, then claims) → TRANSFORMATION (paint the after — the real desire fulfilled) → OFFER + RISK-REVERSAL → CTA (one clear next step).

8. BUILD BELIEF BY GRADUALIZATION. Don't demand the big belief up front. Lead them one believable step at a time; each agreed claim makes the next easier. Every claim needs a reason-to-believe.

9. PROOF THEY ALREADY OWN beats proof you assert. The strongest evidence is a lived experience they cannot deny, reattributed to your mechanism.

10. SPECIFICITY & DIMENSIONALIZATION. Concrete, sensory, exact. Make them SEE and FEEL the precise moment ("you flex in the mirror at 9pm and see nothing") — never generic ("you're unhappy with your body").

11. ONE BIG IDEA. One ad = one desire, one mechanism, one promise. Subtract everything that isn't the idea.
`.trim();

